exports.getMyTours = catchAsync(async (req, res, next) => {
  //Se buscan todas las reservas (Bookings) del usuarios logeado
  const bookings = await Booking.find({ user: req.user.id });

  //2. Se encuentran los tours con los IDs obtenidos de las reservas
  const tourIDs = bookings.map(el => el.tour);
  //El $in es para que encuentre todos los tours del arreglo de IDs
  const tours = await Tour.find({ _id: { $in: tourIDs } });

  res.status(200).render('overview', {
    title: 'My tours',
    tours
  });
});

// Actualizar los datos del usuario sin usar la API
exports.updateUserData = catchAsync(async (req, res, next) => {
  const updatedUser = await User.findByIdAndUpdate(
    req.user.id,
    {
      name: req.body.name,
      email: req.body.email
    },
    {
      new: true,
      runValidators: true
    }
  );

  res.status(200).render('account', {
    title: 'Your account',
    user: updatedUser
  });
});
